const langToggle = document.getElementById("langToggle");

function setLanguage(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll(".lang-es").forEach((element) => {
    element.style.display = lang === "es" ? "" : "none";
  });

  document.querySelectorAll(".lang-en").forEach((element) => {
    element.style.display = lang === "en" ? "" : "none";
  });

  langToggle.textContent = lang === "es" ? "EN" : "ES";
  localStorage.setItem("rodai_lang", lang);
}

langToggle.addEventListener("click", () => {
  const currentLang = localStorage.getItem("rodai_lang") || "es";
  setLanguage(currentLang === "es" ? "en" : "es");
});

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("rodai_lang") || "es";
  setLanguage(savedLang);
});
